function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return b !== 0 ? a / b : null;
}

function isEven(n) {
  return n % 2 === 0;
}

function greet(name) {
  return `Hello, ${name}!`;
}

function square(n) {
  return n * n;
}
